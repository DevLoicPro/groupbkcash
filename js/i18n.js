class LanguageSwitcher {
    constructor() {
        this.currentLang = localStorage.getItem('groupbk_lang') || 'fr';
        this.init();
    }

    init() {
        document.documentElement.lang = this.currentLang;

        // Run immediately if DOM is already loaded, otherwise wait
        if (document.readyState === 'loading') {
            document.addEventListener('DOMContentLoaded', () => this.applyTranslations());
        } else {
            this.applyTranslations();
        }

        // Setup click outside to close dropdowns
        document.addEventListener('click', (e) => {
            if (!e.target.closest('.lang-dropdown')) {
                document.querySelectorAll('.lang-dropdown-menu').forEach(m => m.classList.remove('active'));
                document.querySelectorAll('.lang-dropdown-trigger').forEach(m => m.classList.remove('active'));
            }
        });

        // Sync with other tabs
        window.addEventListener('storage', (e) => {
            if (e.key === 'groupbk_lang' && e.newValue !== this.currentLang) {
                this.switch(e.newValue, false);
            }
        });
    }

    switch(lang, broadcast = true) {
        this.currentLang = lang;
        if (broadcast) localStorage.setItem('groupbk_lang', lang);

        document.documentElement.lang = lang;
        this.applyTranslations();
        document.dispatchEvent(new CustomEvent('languageChanged', { detail: lang }));

        // Close menu
        document.querySelectorAll('.lang-dropdown-menu').forEach(m => m.classList.remove('active'));
        document.querySelectorAll('.lang-dropdown-trigger').forEach(m => m.classList.remove('active'));
    }

    applyTranslations() {
        const texts = TRANSLATIONS[this.currentLang] || TRANSLATIONS['fr'];
        const fallbackTexts = TRANSLATIONS['fr'];

        // Translate page title and meta description if keys exist
        if (texts.site_title || fallbackTexts.site_title) {
            document.title = texts.site_title || fallbackTexts.site_title;
        }

        const metaDesc = document.querySelector('meta[name="description"]');
        if (metaDesc && (texts.site_description || fallbackTexts.site_description)) {
            metaDesc.setAttribute('content', texts.site_description || fallbackTexts.site_description);
        }

        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n').trim();
            const translation = texts[key] || fallbackTexts[key];

            if (translation) {
                if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                    el.placeholder = translation;
                } else if (el.hasAttribute('data-i18n-title')) {
                    el.title = translation;
                } else {
                    el.innerHTML = translation;
                }
            }
        });
    }

    /**
     * Helper to get a translation key from JS code
     * @param {string} key 
     * @returns {string}
     */
    t(key) {
        const texts = TRANSLATIONS[this.currentLang] || TRANSLATIONS['fr'];
        const fallbackTexts = TRANSLATIONS['fr'];
        return texts[key] || fallbackTexts[key] || key;
    }

    toggleDropdown(e) {
        e.stopPropagation();
        const parent = e.currentTarget.closest('.lang-dropdown');
        const menu = parent.querySelector('.lang-dropdown-menu');
        const trigger = parent.querySelector('.lang-dropdown-trigger');

        // Close others
        document.querySelectorAll('.lang-dropdown-menu').forEach(m => {
            if (m !== menu) m.classList.remove('active');
        });
        document.querySelectorAll('.lang-dropdown-trigger').forEach(t => {
            if (t !== trigger) t.classList.remove('active');
        });

        menu.classList.toggle('active');
        trigger.classList.toggle('active');
    }

    getSwitcherHTML() {
        const current = TRANSLATIONS[this.currentLang] || TRANSLATIONS['fr'];
        const langs = Object.keys(TRANSLATIONS).map(code => ({
            code,
            ...TRANSLATIONS[code]
        }));

        let html = `
            <div class="lang-dropdown">
                <div class="lang-dropdown-trigger" onclick="i18n.toggleDropdown(event)">
                    <span class="lang-flag">${current.flag}</span>
                    <span class="lang-label-active">${current.label}</span>
                    <i data-lucide="chevron-down" class="lang-chevron"></i>
                </div>
                <ul class="lang-dropdown-menu">
        `;

        langs.forEach(l => {
            html += `
                <li class="${this.currentLang === l.code ? 'selected' : ''}" onclick="i18n.switch('${l.code}')">
                    <span class="lang-flag">${l.flag}</span>
                    <span class="lang-name">${l.label}</span>
                </li>
            `;
        });

        html += `
                </ul>
            </div>
        `;
        return html;
    }
}

window.TRANSLATIONS = TRANSLATIONS;
window.i18n = new LanguageSwitcher();
